class Stats {
    hp: number;
    atk: number;
    def: number;
    wis: number;
    agi: number;

    constructor(hp: number, atk: number, def: number, wis: number, agi: number) {
        this.hp = hp;
        this.atk = atk;
        this.def = def;
        this.wis = wis;
        this.agi = agi;
    }
} 
